foreach ($i in 2..11) {
    $classDir = "clase $i"
    $htmlFile = Join-Path $classDir "clase$i.html"
    $jsFile = Join-Path $classDir "script.js"

    if (Test-Path $htmlFile) {
        $html = Get-Content $htmlFile -Raw -Encoding UTF8
        $html = $html -replace 'INGENIERÍA EN COMPUTACIÓN', 'INGENIERÍA EN SONIDO'
        $html = $html -replace 'Ingeniería en computación', 'Ingeniería en sonido'
        Set-Content $htmlFile $html -Encoding UTF8
    }

    if (Test-Path $jsFile) {
        $js = Get-Content $jsFile -Raw -Encoding UTF8
        
        # 1. Replace VOCABULARY
        $vocabItems = $sonidoVocab[$i]
        $vocabJson = "["
        foreach ($item in $vocabItems) {
            $w = $item.word
            $m = $item.meaning
            $vocabJson += "`r`n    { word: '$w', meaning: '$m', category: 'default' },"
        }
        $vocabJson = $vocabJson.TrimEnd(',') + "`r`n]"
        
        $js = [regex]::Replace($js, 'const VOCABULARY = \[[\s\S]*?\];', "const VOCABULARY = $vocabJson;")

        # 2. Update initAILab context
        $js = $js -replace 'contexto de Ingeniería Informática', 'contexto de Ingeniería en Sonido'
        $js = $js -replace 'Ingeniería Informática', 'Ingeniería en Sonido'
        $js = $js -replace 'técnico de Ingeniería Informática', 'técnico de Ingeniería en Sonido'

        # 3. Handle Crossword (Simplified: horizontal layout to avoid broken complex grids)
        # We will create a vertical list of words for the crossword
        $crosswordDataJson = "["
        $y = 1
        foreach ($item in $vocabItems) {
            $word = $item.word.ToUpper().Replace(" ", "").Replace("-", "")
            if ($word.Length -gt 0) {
                $clue = $item.meaning
                $crosswordDataJson += "`r`n            { word: '$word', x: 1, y: $y, dir: 'across', clue: '$clue' },"
                $y += 2
            }
        }
        $crosswordDataJson = $crosswordDataJson.TrimEnd(',') + "`r`n        ]"
        
        $js = [regex]::Replace($js, 'const crosswordData = \[[\s\S]*?\];', "const crosswordData = $crosswordDataJson;")

        Set-Content $jsFile $js -Encoding UTF8
    }
}
